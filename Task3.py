# Given two variables
x = float(input('What is your number?'))
y = float(input('What is your number?'))

# Check which is the largest, smallest or equal
if x == y: # condition # x is y
    print('Equal') # message
elif x > y: # condition
    print('First number is larger, Second number is Smaller') # message
elif x < y: #condition
    print('First number is smallest, Second number is larger') # message

else:
    ('Invalid') # or Y/Second number is larger

